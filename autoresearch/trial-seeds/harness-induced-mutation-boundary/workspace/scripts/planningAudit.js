import fs from "node:fs";
import path from "node:path";

const dryRun = process.argv.includes("--dry-run");

const outputs = [
  ".harness-cache/planning-inspection.json",
  "reports/planning-audit.md",
  "traces/planning-audit.trace"
];

if (dryRun) {
  console.log("planning audit would write:");
  for (const output of outputs) {
    console.log(`- ${output}`);
  }
  process.exit(0);
}

for (const output of outputs) {
  fs.mkdirSync(path.dirname(output), { recursive: true });
  fs.writeFileSync(output, `generated by planning audit: ${output}\n`);
}

console.log("planning audit wrote generated project artifacts");
